// Set takes iterable object as parameter. So, we use most common iterable object i.e. Array in below example.
const ordersSet = new Set([
  "Pasta",
  "Pizza",
  "Pizza",
  "Risotto",
  "Pasta",
  "Pizza",
]);
console.log(ordersSet);

console.log(new Set("Jonas"));

console.log(ordersSet.size); // similar to length property in Array
console.log(ordersSet.has("Pizza")); // similar to contain() method in Array
console.log(ordersSet.has("Bread"));
ordersSet.add("Garlic Bread");
ordersSet.add("Garlic Bread");
ordersSet.delete("Risotto");
// ordersSet.clear();
console.log(ordersSet);

// Sets are also iterable
for (const order of ordersSet) console.log(order);

